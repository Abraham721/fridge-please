// vocab 빌드(중복 제거, 길이 내림차순 — "양송이버섯"이 "버섯"보다 먼저 매칭되도록)
const _seen = new Set()
let _vocab = []
try {
  for (const r of RECIPE_DB || []) for (const ing of (r.ingredients || [])) {
    const n = String(ing).trim(); if (n && !_seen.has(n)) { _seen.add(n); _vocab.push(n) }
  }
} catch (e) {}
for (const w of COMMON_EXTRA) if (!_seen.has(w)) { _seen.add(w); _vocab.push(w) }
_vocab.sort((a, b) => b.length - a.length)
export const INGREDIENT_VOCAB = _vocab

// 부정 표현: 매칭 어휘 바로 뒤 ~10자에 등장하면 제외.
const NEG_RE = /없|안\s?있|빼|제외|안\s?넣/

// 음성 텍스트 → 추출 재료(중복 없는 배열, 순서 유지).
export function parseVoiceIngredients(transcript) {
  if (!transcript) return []
  let scan = String(transcript).toLowerCase().replace(/\s+/g, ' ')
  const out = []; const seen = new Set()
  for (const v of INGREDIENT_VOCAB) {
    const lv = v.toLowerCase()
    let idx = scan.indexOf(lv)
    while (idx !== -1) {
      const after = scan.slice(idx + lv.length, idx + lv.length + 10)
      if (NEG_RE.test(after)) {
        // 부정형 — 마스킹 후 다음 위치
        scan = scan.slice(0, idx) + ' '.repeat(lv.length) + scan.slice(idx + lv.length)
        idx = scan.indexOf(lv); continue
      }
      if (!seen.has(v)) { seen.add(v); out.push(v) }
      // 매칭 자리는 마스킹해 sub-매칭(예: 양송이버섯의 '버섯') 방지
      scan = scan.slice(0, idx) + ' '.repeat(lv.length) + scan.slice(idx + lv.length)
      idx = scan.indexOf(lv)
    }
  }
  return out
}
