// 셰프의 시즌2 대표 요리명 N개 (페르소나 grounding용).
// dishes가 이미 high·개인전 우선으로 정렬돼 있어 앞에서부터 뽑으면 확정·구체 요리가 먼저 온다.
// 부연설명(— 뒤)·괄호·따옴표 코멘트는 제거하고 핵심 요리명만 추려 중복 없이 반환.
export function season2Signatures(id, n = 6) {
  const c = id && SEASON2_CHEFS[id]
  if (!c) return []
  const seen = new Set(); const out = []
  for (const d of c.dishes) {
    const name = d.dish
      .split(' — ')[0]
      .replace(/\([^)]*\)/g, '')
      .replace(/['"][^'"]*['"]/g, '')
      .replace(/\s+/g, ' ')
      .trim()
    if (!name || seen.has(name)) continue
    seen.add(name); out.push(name)
    if (out.length >= n) break
  }
  return out
}
